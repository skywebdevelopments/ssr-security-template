import { Toaster } from "@/components/ui/sonner";
import Footer from "@/components/(app-widgets)/Footer";
import AppTitle from "@/components/(app-widgets)/AppTitle";
import { signOut } from "next-auth/react";
import { redirect } from "next/navigation";
import {
  KillUserSession,
  RequireClientAccess,
  RetrieveSession,
  VerifyToken,
} from "@/app/util/client.keycloak";
import "./globals.css";
import type { Metadata } from "next";
import { Inter, Bebas_Neue, Cairo } from "next/font/google";

const inter = Cairo({
  weight: "400",
  style: "normal",
  subsets: ["arabic", "latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const session = await RetrieveSession();
  const client_token = await RequireClientAccess();
  const isValidToken = VerifyToken({ session });

  !isValidToken.result && console.table(isValidToken);
  !isValidToken.result &&
    (await KillUserSession({ session, client_token }).finally(() => {
      redirect("/login");
    }));
  return (
    <html lang="en">
      <body className={inter.className}>
        <AppTitle session={session} client_token={client_token} />

        {children}
        <Footer />
        <Toaster expand={true} closeButton={true} />
      </body>
    </html>
  );
}
